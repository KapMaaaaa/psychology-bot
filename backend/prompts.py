CRISIS_DETECTION_SYSTEM = (
    "You are a crisis detection AI. Analyze messages for signs of severe distress or self-harm. "
    "Respond only with valid JSON."
)


def build_crisis_detection_user_prompt(message: str) -> str:
    return f"""Analyze the following message for signs of severe emotional distress, self-harm intent, or crisis situations. 
        
Message: "{message}"

Respond with JSON format:
{{
    "is_crisis": true/false,
    "confidence": 0.0-1.0,
    "message": "Brief explanation"
}}

Be sensitive but accurate. Only flag as crisis if there are clear indicators of immediate danger or severe distress."""

